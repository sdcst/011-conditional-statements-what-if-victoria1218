# Some different examples of the if statement

a = 3

# check to see if the number is 4
if a == 4:
    print("the number is 4")

#check to see if the number is larger than 4
if a > 4:
    print("the number is larger than 4")

#check to see if the number is 4 or larger
#note that  if a => 4 does NOT do the same thing
if a >= 4:
    print("the number is 4 or largeer")

#check to see if the number is not 4
if a !=4:
    print('the number is not 4')