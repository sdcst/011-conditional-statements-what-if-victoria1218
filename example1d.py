#Python is one of the few languages that allows you to do multiple comparisons, checking to see if a number is 
# between 2 values

#check to see if a number is between 5 and 10
num = 8
if 5 < num < 10:
    print(f"{num} is between 5 and 10")



#check to see if a number is greater than or equal to 5
#and also less than or equal to 10
num = 9
if 5 <= num <= 10:
    print(f"the statement 5 <= {num} <= 10 is true")